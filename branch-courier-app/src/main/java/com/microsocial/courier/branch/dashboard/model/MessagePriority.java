package com.gogidix.courier.branch.dashboard.model;

/**
 * Enum defining different priority levels for dashboard messages.
 */
public enum MessagePriority {
    
    // Critical/emergency messages requiring immediate attention
    CRITICAL,
    
    // High priority messages that should be processed promptly
    HIGH,
    
    // Normal priority messages (default)
    NORMAL,
    
    // Low priority informational messages
    LOW
} 